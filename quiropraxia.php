					<div class="subtitulo-texto">Sobre o Tratamento</div>
					<p class="recuo">Segundo a Organização Mundial de Saúde (OMS), a Quiropraxia é uma profissão da saúde que lida com o diagnostico, tratamento e a prevenção das desordens do sistema neuro-músculo-esquelético e dos efeitos destas desordens na saúde em geral. Há uma ênfase em técnicas manuais, incluindo o ajuste e/ou a manipulação articular, com um enfoque particular nas subluxações.</p>

					<p class="recuo">Os conceitos e os princípios que distinguem e diferenciam a filosofia da Quiropraxia de outras profissões de saúde são de grande importância para a maioria dos quiropraxistas e influenciam profundamente a atitude e a abordagem destes em relação à atenção à saúde.</p>

					<p class="recuo">A relação entre a estrutura, particularmente a coluna vertebral e o sistema músculo-esquelético, e a função, especialmente coordenadas pelo sistema nervoso, constitui a essência da Quiropraxia e o seu enfoque para a restauração e preservação da saúde.</p>

					<p class="recuo">Hipoteticamente, conseqüências neurofisiológicas significativas podem ocorrer como resultado de distúrbios funcionais mecânicos da coluna vertebral, descritos pelos quiropraxistas através do termo subluxação ou complexo de subluxação.</p>

